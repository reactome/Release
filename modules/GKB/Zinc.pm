use strict;
use HTTP::Tiny;
use CGI 'url';
use Bio::Root::Root;
use Data::Dumper;

use GKB::Config;

use Log::Log4perl qw/get_logger/;
Log::Log4perl->init(\$LOG_CONF);

# Use our RESTful API to grab a list of small molecules and ChEBI IDs
use constant MOLECULES => '/ReactomeRESTfulAPI/RESTfulWS/getReferenceMolecules';

# Presumably updated mapping file
use constant CHEBI_URL => 'http://zinc.docking.org/db/byvendor/chebi/chebi_purch.xls';

use vars '@ISA';
@ISA = qw(Bio::Root::Root);

sub new {
    my $class = shift;
    my $self  = bless {}, $class;
    my $dba   = shift || $self->throw("The GKB::Zinc class requires a DBA object");
    $self->dba($dba);
    return $self;
}

sub dba {
    my $self = shift;
    my $dba  = shift;
    $self->{dba} = $dba if $dba;
    return $self->{dba};
}

sub fetch_molecules {
    my $self = shift;
    
    my $logger = get_logger(__PACKAGE__);
    
    my $host = url(-base => 1);
    my $url = $host . MOLECULES;
    my $chebi2zinc = $self->map_chebi_to_zinc();
    
    my @data = $self->fetch_url_content($url);
    my %molecules;
    
    for (@data) {
	my ($db_id,$chebi) = split;
	$chebi =~ s/\D+//;

	# We only want molecules that map to ZINC IDs
	next unless $chebi2zinc->{$chebi};

	push @{$molecules{$chebi}}, $self->get_cached_instance($db_id);
    }

    # There can be more than one ZINC ID associated with a chebi ID
    # and more than once ReferenceMolecule associated with a chebi ID!
    my %zinc;
    for my $chebi_id (keys %molecules) {
	my $zinc_ids = $chebi2zinc->{$chebi_id};
	my $instances = $molecules{$chebi_id};
	for my $zinc_id (@$zinc_ids) {
	    for my $instance (@$instances) {
		push @{$zinc{$zinc_id}}, $instance;
	    }
	}
    }

    my $total = keys %zinc;
    $logger->info("I am dealing with a total of about $total ZINC IDS!\n");
    return \%zinc;
}

sub get_instance {
    my $self  = shift;
    my $db_id = shift;
    return $self->dba->fetch_instance_by_db_id($db_id)->[0];
}

sub get_cached_instance {
    my $self  = shift;
    my $db_id = shift;
    
    $self->{cached_instance}->{$db_id} ||= $self->get_instance($db_id);

    return $self->{cached_instance}->{$db_id};
}

# pulls a static file provided by http://zinc.docking.org
# to get current ZINC->ChEBI mapping
sub map_chebi_to_zinc {
    my $self = shift;
    my %chebi2zinc;
    my @data = $self->fetch_url_content(CHEBI_URL);
    for (@data) {
	next unless /ChEBI/;
	my ($chebi,$zinc) = grep {s/\D+//g} (split "\t")[2,0];
	push @{$chebi2zinc{$chebi}}, $zinc;
    }
    return \%chebi2zinc;
}

# Simple HTTP query, expects plain text
sub fetch_url_content {
    my $self = shift;
    my $url = shift;

    my $response = HTTP::Tiny->new->get($url);

    if ($response->{success}) {
	return split("\n",$response->{content});
    }
    else {
	$self->throw(join("\t",$response->{status},$response->{reason}));
    }
}

# subset of proteins that are actually ZINC targets
sub fetch_proteins {
    my $self = shift;
    my %proteins;
    while (<DATA>) {
	chomp;
	$proteins{$_}++;
    }
    return \%proteins;
}

